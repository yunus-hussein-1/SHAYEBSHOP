const CATEGORY_I18N = {
  "إلكترونيات": { ar: "إلكترونيات", en: "Electronics" },
  "ألبسة نسائية": { ar: "ألبسة نسائية", en: "Women's Fashion" },
  "ألبسة رجالية": { ar: "ألبسة رجالية", en: "Men's Fashion" },
  "ألبسة أطفال": { ar: "ألبسة أطفال", en: "Kids Fashion" }
};

function storelyT(key) {
  const lang = storelyGetLang();
  return APP_I18N[lang]?.[key] ?? APP_I18N.ar[key] ?? key;
}

function storelyCategoryLabel(category) {
  const lang = storelyGetLang();
  return CATEGORY_I18N[category]?.[lang] ?? category;
}

function storelySiteName() {
  const lang = storelyGetLang();
  if (lang === "en") return window.APP_CONFIG?.siteName || APP_I18N.en.siteName;
  return window.APP_CONFIG?.siteNameAr || APP_I18N.ar.siteName;
}

function storelyApplyLang() {
  const lang = storelyGetLang();
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "en" ? "ltr" : "rtl";
  document.title = document.title.replace(/متجر الشايب|Alshayeb Store/g, storelySiteName());
  return lang;
}

function storelyApplyAuthPage() {
  storelyApplyLang();
  const map = [
    ["authBrandTitle", "siteName"],
    ["authBrandSub", "siteTagline"],
    ["authLead", "authLead"],
    ["googleLoginBtn", "googleLogin"],
    ["authDivider", "or"],
    ["authTabLogin", "login"],
    ["authTabSignup", "newAccount"],
    ["loginEmail", "email", "placeholder"],
    ["loginPassword", "password", "placeholder"],
    ["signupName", "firstName", "placeholder"],
    ["signupEmail", "email", "placeholder"],
    ["signupPassword", "password", "placeholder"],
    ["forgotLink", "forgotPassword"],
    ["loginSubmit", "login"],
    ["signupSubmit", "createAccount"],
    ["authBack", "backHome"]
  ];
  map.forEach(([id, key, attr]) => {
    const el = document.getElementById(id);
    if (!el) return;
    if (attr === "placeholder") el.placeholder = storelyT(key);
    else el.textContent = storelyT(key);
  });
}

function storelyApplyHelpPage() {
  storelyApplyLang();
  const ids = {
    helpTitle: "helpTitle",
    helpLead: "helpLead",
    helpHowTitle: "howItWorks",
    helpPayTitle: "paymentTitle",
    helpPayLead: "paymentLead",
    helpContactTitle: "contact"
  };
  Object.entries(ids).forEach(([id, key]) => {
    const el = document.getElementById(id);
    if (el) el.textContent = storelyT(key);
  });
  const steps = document.getElementById("helpSteps");
  if (steps) {
    steps.innerHTML = `
      <li>${storelyT("helpBrowse")}</li>
      <li>${storelyT("helpBuy")}</li>
      <li>${storelyT("helpProfile")}</li>
      <li>${storelyT("helpSettings")}</li>
      <li>${storelyT("helpForgot")}</li>`;
  }
  const payList = document.getElementById("helpPayList");
  if (payList) {
    payList.innerHTML = `<li>🤝 ${storelyT("handDelivery")}</li><li>📱 ${storelyT("shamCash")}</li>`;
  }
}

function storelyApplySettingsPage() {
  storelyApplyLang();
  const map = {
    settingsTitle: "settingsTitle",
    settingsLead: "settingsLead",
    passwordSection: "passwordSection",
    currentPasswordLabel: "currentPassword",
    newPasswordLabel: "newPassword",
    confirmPasswordLabel: "confirmPassword",
    changePasswordBtn: "changePassword",
    paymentSection: "paymentOptional",
    savePaymentBtn: "save",
    storeSection: "storeRequest",
    storeSectionLead: "storeRequestLead",
    storeNameLabel: "storeName",
    storeSlugLabel: "storeSlug",
    storeLocationLabel: "storeLocation",
    storeDescLabel: "storeDesc",
    productTitleLabel: "firstProduct",
    productPriceLabel: "priceSyp",
    productCategoryLabel: "category",
    agreeCommissionLabel: "agreeCommission",
    submitStoreBtn: "submitStore"
  };
  Object.entries(map).forEach(([id, key]) => {
    const el = document.getElementById(id);
    if (!el) return;
    if (el.tagName === "LABEL") {
      const input = el.querySelector("input, textarea, select");
      el.childNodes[0].textContent = storelyT(key);
      if (input && key === "storeSlug") input.placeholder = "my-shop";
    } else {
      el.textContent = storelyT(key);
    }
  });
  const paySelect = document.getElementById("paymentMethod");
  if (paySelect) {
    paySelect.options[0].textContent = storelyT("chooseLater");
    paySelect.options[1].textContent = storelyT("handDelivery");
    paySelect.options[2].textContent = storelyT("shamCash");
  }
}
